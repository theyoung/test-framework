class Bean {
    String path;
    String paramsName;
    String paramsYear;

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getParamsName() {
        return paramsName;
    }

    public void setParamsName(String paramsName) {
        this.paramsName = paramsName;
    }

    public String getParamsYear() {
        return paramsYear;
    }

    public void setParamsYear(String paramsYear) {
        this.paramsYear = paramsYear;
    }
}